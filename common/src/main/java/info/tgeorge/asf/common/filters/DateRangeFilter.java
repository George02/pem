package info.tgeorge.asf.common.filters;

/**
 *
 * @author george
 */
public class DateRangeFilter {

    private String from;
    private String to = "10-10-2999";

    public DateRangeFilter() {

    }

    public DateRangeFilter(String from, String to) {
        this.from = from;
        this.to = to;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

}
